/*------------------MEMBER-------------------------*/
/*-------------------------------------------------*/
CREATE TABLE MEMBER (
    USER_NO NUMBER NOT NULL PRIMARY KEY,
    EMAIL VARCHAR2(50) NOT NULL UNIQUE,
    USER_PWD VARCHAR2(200) NOT NULL,
    USER_NAME VARCHAR2(51) NOT NULL,
    BIRTH VARCHAR2(6) NOT NULL,
    GENDER VARCHAR2(1),
    PHONE VARCHAR2(13) NOT NULL,
    ZIPCODE NUMBER NOT NULL,
    ADDRESS1 VARCHAR2(300) NOT NULL,
    ADDRESS2 VARCHAR2(300),
    ADDRESS3 VARCHAR2(300),
    ENROLL_DATE DATE DEFAULT SYSDATE NOT NULL,
    MODIFY_DATE DATE DEFAULT SYSDATE NOT NULL,
    CONNECT_DATE DATE DEFAULT SYSDATE NOT NULL,
    STATUS VARCHAR2(1) DEFAULT 'Y' CHECK (STATUS IN('Y', 'N')) ,
    MEMBER_DIVIDE_NO NUMBER CHECK (MEMBER_DIVIDE_NO IN('1','2','3'))NOT NULL,
    GRADE_NO NUMBER DEFAULT 200 CHECK (GRADE_NO IN('100','200','300','400')) ,
    BLACK_STATUS  VARCHAR2(1) DEFAULT 'N' CHECK (BLACK_STATUS IN('Y', 'N'))
);

COMMENT ON TABLE MEMBER IS '회원정보';
